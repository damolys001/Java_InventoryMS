public class UserCheckWhole {
    
  
    private String pname;
   
    private int whole;
     
    
    
    public UserCheckWhole(String Pname,int Whole)
    {
       
        this.pname = Pname;
       
        this.whole = Whole;
        
    }
   
    
    public String getPname()
    {
        return pname;
    }
    
   
    
    public int getWhole()
    {
        return whole;
    }
   
}


